class Main {
    public static void main(String[] args) {
        int number1 = 20;
        int number2 = 5;
        
        //addition
        System.out.println(number1 + number2); //25

        //subtraction
        System.out.println(number1 - number2); //15

        //division
        System.out.println(number1 / number2); //4

        //multiplication
        System.out.println(number1 * number2); //100

        //remainder/modulo
        System.out.println(number1 % number2); //0

    }
}
